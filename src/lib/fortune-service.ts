  /**
   * สุ่มคำทำนาย/ข้อคิดดีๆ
   */
  static getRandomFortune(): string {
    const prefix = this.FORTUNE_PREFIXES[Math.floor(Math.random() * this.FORTUNE_PREFIXES.length)];
    const message = this.FORTUNE_MESSAGES[Math.floor(Math.random() * this.FORTUNE_MESSAGES.length)];
    const suffix = this.FORTUNE_SUFFIXES[Math.floor(Math.random() * this.FORTUNE_SUFFIXES.length)];

    return `${prefix}\n\n${message}\n\n${suffix}`;
  }

  /**
   * คำทำนายพิเศษเกี่ยวกับการเงิน
   */
  static getFinancialFortune(): string {
    const financialMessages = this.FORTUNE_MESSAGES.filter(msg => 
      msg.includes('เงิน') || msg.includes('การเงิน') || msg.includes('ออม') || msg.includes('ลงทุน')
    );
    
    const prefix = "💰 คำทำนายการเงินจากแมวโชคลาภ...";
    const message = financialMessages[Math.floor(Math.random() * financialMessages.length)];
    const suffix = "ขอให้ร่ำรวยนะเหมียว! 💎";

    return `${prefix}\n\n${message}\n\n${suffix}`;
  }

  /**
   * ข้อความสำหรับเวลาต่างๆ ในวัน
   */
  static getTimeBasedFortune(): string {
    const hour = new Date().getHours();
    let timeMessage = "";

    if (hour >= 5 && hour < 12) {
      timeMessage = "🌅 สวัสดีตอนเช้า! พลังงานใหม่ๆ กำลังจะเข้ามาในชีวิตคุณ";
    } else if (hour >= 12 && hour < 17) {
      timeMessage = "☀️ บ่ายนี้เป็นช่วงเวลาที่ดีสำหรับการทำงาน มุ่งมั่นต่อไปนะ";
    } else if (hour >= 17 && hour < 21) {
      timeMessage = "🌆 ตอนเย็นแล้ว ได้เวลาพักผ่อนและทบทวนสิ่งดีๆ ที่เกิดขึ้นวันนี้";
    } else {
      timeMessage = "🌙 ราตรีสวัสดิ์ หลับให้สบายแล้วฝันดีนะ";
    }

    const suffix = this.FORTUNE_SUFFIXES[Math.floor(Math.random() * this.FORTUNE_SUFFIXES.length)];
    return `${timeMessage}\n\n${suffix}`;
  }
